// Arithmetic Operator
let sum = 10 + 5;
let difference = 10 - 5;
let product = 10 * 5;
let quotient = 10 / 5;
console.log("\n1. Arithmetic Operators");
console.log("Sum = " + sum);
console.log("Difference = " + difference);
console.log("Product = " + product);
console.log("Quotient = " + quotient);

// Comparison Operator
let isEqual = (10 == 10); 
let isIdentical = (10 === 10); 
let isGreaterThan = (10 > 5); 

console.log("\n2. Comparison Operators");
console.log("isEqual = " + isEqual);
console.log("isIdentical = " + isIdentical);
console.log("isGreaterThan = " + isGreaterThan);

// Logical Operator
let and = (true && false); 
let or = (true || false); 
let not = !true; 

console.log("\n3. Logical Operators");
console.log("AND = " + and);
console.log("OR = " + or);
console.log("NOT = " + not);

// Assignment Operator
console.log("\n4. Assignment Operators");
let x = 10;
console.log("Value of x = " + x);
x += 5; 
console.log("Value of x = " + x);
x -= 3; 
console.log("Value of x = " + x);
