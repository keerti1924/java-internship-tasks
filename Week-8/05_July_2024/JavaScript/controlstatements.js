// If-else
console.log("\n1. If-Else Statement \n");
let number = 10;
if (number > 5) {
  console.log("Number is greater than 5");
} else {
  console.log("Number is not greater than 5");
}

// Switch
console.log("\n2. Switch Statement \n");
let fruit = "apple";
switch (fruit) {
  case "banana":
    console.log("This is a banana");
    break;
  case "apple":
    console.log("This is an apple");
    break;
  default:
    console.log("Unknown fruit");
}

// For loop
console.log("\n3. For Loop \n");
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// While loop
console.log("\n4. While Loop \n");
let count = 5;
while (count <= 10) {
  console.log(count);
  count++;
}
